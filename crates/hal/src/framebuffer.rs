//! Pixel framebuffer text console — renders bitmap font characters.
//!
//! This module implements an 8×16 bitmap font text console on top of
//! a raw pixel framebuffer. It can be adapted for any system that provides
//! a linear framebuffer (UEFI, bootloader, embedded display).
//!
//! ## Reuse Guide
//! The core algorithm (font lookup + pixel drawing + scroll) is ~150 lines
//! and depends only on a `&mut [u8]` framebuffer slice. To reuse:
//! 1. Copy `font_glyph()` and `FONT_DATA`
//! 2. Implement `put_pixel()` for your framebuffer format
//! 3. Call `draw_char()` for each character

use core::fmt;
use spin::Mutex;

const FONT_WIDTH: usize = 8;
const FONT_HEIGHT: usize = 16;

/// Global framebuffer console writer.
pub static CONSOLE: Mutex<Option<FramebufferConsole>> = Mutex::new(None);

/// Pixel-based text console backed by the bootloader's framebuffer.
pub struct FramebufferConsole {
    buffer: &'static mut [u8],
    width: usize,
    height: usize,
    bytes_per_pixel: usize,
    stride: usize,
    cols: usize,
    rows: usize,
    cursor_col: usize,
    cursor_row: usize,
    fg: [u8; 3],
    bg: [u8; 3],
}

impl FramebufferConsole {
    /// Initialise with the bootloader-provided framebuffer.
    ///
    /// # Safety
    /// `buffer` must be a valid, exclusively-owned slice covering the
    /// entire framebuffer memory region for the lifetime of the console.
    pub unsafe fn new(
        buffer: &'static mut [u8],
        width: usize,
        height: usize,
        bytes_per_pixel: usize,
        stride: usize,
    ) -> Self {
        let cols = width / FONT_WIDTH;
        let rows = height / FONT_HEIGHT;
        let mut console = Self {
            buffer,
            width,
            height,
            bytes_per_pixel,
            stride,
            cols,
            rows,
            cursor_col: 0,
            cursor_row: 0,
            fg: [0xCC, 0xDD, 0xFF], // light blue (BGR)
            bg: [0x1E, 0x1E, 0x2E], // dark background (BGR)
        };
        console.clear();
        console
    }

    /// Sets the foreground text color (BGR byte order).
    pub fn set_fg_color(&mut self, bgr: [u8; 3]) {
        self.fg = bgr;
    }

    /// Fills the entire screen with the background colour.
    pub fn clear(&mut self) {
        for y in 0..self.height {
            for x in 0..self.width {
                self.put_pixel(x, y, self.bg);
            }
        }
        self.cursor_col = 0;
        self.cursor_row = 0;
    }

    /// Writes a single ASCII byte at the cursor, advancing it.
    pub fn write_byte(&mut self, byte: u8) {
        match byte {
            b'\n' => self.newline(),
            b'\r' => self.cursor_col = 0,
            0x08 => {
                if self.cursor_col > 0 {
                    self.cursor_col -= 1;
                    self.draw_char(self.cursor_row, self.cursor_col, b' ');
                }
            }
            byte => {
                if self.cursor_col >= self.cols {
                    self.newline();
                }
                self.draw_char(self.cursor_row, self.cursor_col, byte);
                self.cursor_col += 1;
            }
        }
    }

    fn newline(&mut self) {
        self.cursor_col = 0;
        if self.cursor_row + 1 < self.rows {
            self.cursor_row += 1;
        } else {
            self.scroll_up();
        }
    }

    /// Scrolls the framebuffer up by one text row using bulk `copy_within`.
    fn scroll_up(&mut self) {
        let row_bytes = self.stride * self.bytes_per_pixel * FONT_HEIGHT;
        let total = self.stride * self.bytes_per_pixel * self.height;
        self.buffer.copy_within(row_bytes..total, 0);
        // Clear last row with background color using stride-aware iteration
        let start = total - row_bytes;
        for i in (start..total).step_by(self.bytes_per_pixel) {
            if i + 2 < self.buffer.len() {
                self.buffer[i] = self.bg[0];
                self.buffer[i + 1] = self.bg[1];
                self.buffer[i + 2] = self.bg[2];
            }
        }
    }

    fn draw_char(&mut self, row: usize, col: usize, ch: u8) {
        let glyph = font_glyph(ch);
        let base_x = col * FONT_WIDTH;
        let base_y = row * FONT_HEIGHT;

        for (dy, &glyph_row) in glyph.iter().enumerate() {
            for dx in 0..FONT_WIDTH {
                let on = (glyph_row >> (7 - dx)) & 1 == 1;
                let color = if on { self.fg } else { self.bg };
                self.put_pixel(base_x + dx, base_y + dy, color);
            }
        }
    }

    fn put_pixel(&mut self, x: usize, y: usize, color: [u8; 3]) {
        if x >= self.width || y >= self.height {
            return;
        }
        let offset = y * self.stride * self.bytes_per_pixel + x * self.bytes_per_pixel;
        if offset + 2 < self.buffer.len() {
            self.buffer[offset] = color[0];
            self.buffer[offset + 1] = color[1];
            self.buffer[offset + 2] = color[2];
        }
    }
}

impl fmt::Write for FramebufferConsole {
    fn write_str(&mut self, s: &str) -> fmt::Result {
        for byte in s.bytes() {
            self.write_byte(byte);
        }
        Ok(())
    }
}

/// Initialises the global framebuffer console from bootloader info.
///
/// # Safety
/// Must be called exactly once with a valid framebuffer from `BootInfo`.
pub unsafe fn init(
    buffer: &'static mut [u8],
    width: usize,
    height: usize,
    bytes_per_pixel: usize,
    stride: usize,
) {
    let console =
        unsafe { FramebufferConsole::new(buffer, width, height, bytes_per_pixel, stride) };
    *CONSOLE.lock() = Some(console);
}

/// Sets the framebuffer text color. Colors are in BGR byte order.
pub fn set_color(bgr: [u8; 3]) {
    x86_64::instructions::interrupts::without_interrupts(|| {
        if let Some(ref mut console) = *CONSOLE.lock() {
            console.set_fg_color(bgr);
        }
    });
}

/// Predefined colors (BGR order for the framebuffer's Bgr pixel format).
pub mod colors {
    /// Light blue — default text color.
    pub const DEFAULT: [u8; 3] = [0xFF, 0xDD, 0xCC];
    /// Green — success messages and prompt.
    pub const GREEN: [u8; 3] = [0x66, 0xE6, 0xA6];
    /// Red — error messages.
    pub const RED: [u8; 3] = [0x66, 0x66, 0xF3];
    /// Yellow — warnings.
    pub const YELLOW: [u8; 3] = [0x66, 0xE0, 0xF9];
    /// White — bright text.
    pub const WHITE: [u8; 3] = [0xF4, 0xDD, 0xCD];
}

/// Writes formatted text to the framebuffer console.
#[doc(hidden)]
pub fn _fb_print(args: fmt::Arguments) {
    use fmt::Write;
    x86_64::instructions::interrupts::without_interrupts(|| {
        if let Some(ref mut console) = *CONSOLE.lock() {
            let _ = console.write_fmt(args);
        }
    });
}

