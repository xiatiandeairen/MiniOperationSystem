/// Returns the 16-byte glyph bitmap for an ASCII character.
/// Each byte represents one row; bit 7 is the leftmost pixel.
fn font_glyph(ch: u8) -> &'static [u8; 16] {
    let idx = ch as usize;
    if (0x20..=0x7E).contains(&idx) {
        &FONT_DATA[idx - 0x20]
    } else {
        &FONT_DATA[0] // space for unprintable
    }
}

